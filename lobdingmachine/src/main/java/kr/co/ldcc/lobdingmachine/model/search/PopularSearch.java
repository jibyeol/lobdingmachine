package kr.co.ldcc.lobdingmachine.model.search;

public class PopularSearch {

	String keyword;
	int count;
	
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}